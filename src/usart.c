#if EN_USART2_RX   //如果使能了接收
//串口1中断服务程序
//注意,读取USARTx->SR能避免莫名其妙的错误   	
unsigned char USART_RX_BUF[USART_REC_LEN];     //接收缓冲,最大USART_REC_LEN个字节.
//接收状态
//bit15，	接收完成标志
//bit14，	接收到0x0d
//bit13~0，	接收到的有效字节数目
unsigned short USART_RX_STA=0;       //接收状态标记	  
  
void USART2_IRQHandler(void)
{
	unsigned char res;	
#if SYSTEM_SUPPORT_OS 		//如果SYSTEM_SUPPORT_OS为真，则需要支持OS.
	OSIntEnter();    
#endif
	if(USART2->SR&(1<<5))//接收到数据
	{	 
		res=USART2->DR; 
		if((USART_RX_STA&0x8000)==0)//接收未完成
		{
			if(USART_RX_STA&0x4000)//接收到了0x0d
			{
				if(res!=0x0a)USART_RX_STA=0;//接收错误,重新开始
				else USART_RX_STA|=0x8000;	//接收完成了 
			}else //还没收到0X0D
			{	
				if(res==0x0d)USART_RX_STA|=0x4000;
				else
				{
					USART_RX_BUF[USART_RX_STA&0X3FFF]=res;
					USART_RX_STA++;
					if(USART_RX_STA>(USART_REC_LEN-1))USART_RX_STA=0;//接收数据错误,重新开始接收	  
				}		 
			}
		}  		 									     
	} 
#if SYSTEM_SUPPORT_OS 	//如果SYSTEM_SUPPORT_OS为真，则需要支持OS.
	OSIntExit();  											 
#endif
} 
#endif										 
//初始化IO 串口1
//pclk2:PCLK2时钟频率(Mhz)
//bound:波特率 
void uart_init(unsigned int pclk2,unsigned int bound)
{  	 
	float temp;
	unsigned short mantissa;
	unsigned short fraction;	   
	temp=(float)(pclk2*1000000)/(bound*16);//得到USARTDIV@OVER8=0
	mantissa=temp;				 //得到整数部分
	fraction=(temp-mantissa)*16; //得到小数部分@OVER8=0 
    mantissa<<=4;
	mantissa+=fraction; 
	RCC->AHB1ENR|=1<<0;   	//使能PORTA口时钟  
	RCC->APB1ENR|=1<<17;  	//使能串口2时钟 
	
	GPIOA->MODER &= ~((3UL << 2*2 ));
	GPIOA->MODER |= ((2UL << 2*2 ));                /* PA2 is output                     */
	
	GPIOA->OSPEEDR &= ~((3UL << 2*2 ));
	GPIOA->OSPEEDR |= ((2UL << 2*2 ));              /* PA2 is 50MHz Fast Speed           */
	
	GPIOA->OTYPER &= ~((1UL << 2 ));
	GPIOA->OTYPER |= ((0UL << 2 ));               /* PA2 is output Push-Pull           */
	
	GPIOA->PUPDR &= ~((3UL << 2*2 ));
	GPIOA->PUPDR |= ((1UL << 2*2 ));                /* PA2 is Pull down                  */
	
	GPIOA->MODER &= ~((3UL << 2*3 ));
	GPIOA->MODER |= ((2UL << 2*3 ));                /* PA3 is output                     */
	
	//GPIOA->OSPEEDR &= ~((3UL << 2*3 ));
	//GPIOA->OSPEEDR |= ((2UL << 2*3 ));              /* PA3 is 50MHz Fast Speed           */
	
	//GPIOA->OTYPER &= ~((1UL << 3 ));
	//GPIOA->OTYPER |= ((0UL << 3 ));               /* PA3 is output Push-Pull           */
	
	GPIOA->PUPDR &= ~((3UL << 2*3 ));
	GPIOA->PUPDR |= ((0UL << 2*3 ));                /* PA3 is Pull down                  */
	
	
	
	//GPIO_Set(GPIOA,PIN9|PIN10,GPIO_MODE_AF,GPIO_OTYPE_PP,GPIO_SPEED_50M,GPIO_PUPD_PU);//PA9,PA10,复用功能,上拉输出
 	//GPIO_AF_Set(GPIOA,2,7);	//PA2,AF7
	//GPIO_AF_Set(GPIOA,3,7);//PA3,AF7  	   
	GPIOA->AFR[1] |= ((2UL << 4*7 ));
	GPIOA->AFR[1] |= ((3UL << 4*7 ));  
	
	//波特率设置
 	USART2->BRR=mantissa; 	//波特率设置	 
	USART2->CR1&=~(1<<15); 	//设置OVER8=0 
	USART2->CR1|=1<<3;  	//串口发送使能 
#if EN_USART2_RX		  	//如果使能了接收
	//使能接收中断 
	USART2->CR1|=1<<2;  	//串口接收使能
	USART2->CR1|=1<<5;    	//接收缓冲区非空中断使能	    	
	MY_NVIC_Init(3,3,USART2_IRQn,2);//组2，最低优先级 
#endif
	USART2->CR1|=1<<13;  	//串口使能
}














